"""Manifest-based file ownership tracking."""

from pathlib import Path

MANIFEST_FILENAME = ".generated-manifest"


def load_manifest(repo_root: Path) -> set[Path]:
    """Read the manifest and return owned file paths (relative to repo root)."""
    manifest_path = repo_root / MANIFEST_FILENAME
    if not manifest_path.exists():
        return set()

    result: set[Path] = set()
    for line in manifest_path.read_text(encoding="utf-8").splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        result.add(Path(stripped))
    return result


def save_manifest(repo_root: Path, owned: set[Path]) -> None:
    """Write sorted paths with a header comment."""
    manifest_path = repo_root / MANIFEST_FILENAME
    lines = ["# Generated by hassette-codegen — do not edit manually"]
    for path in sorted(owned):
        lines.append(str(path))
    lines.append("")
    manifest_path.write_text("\n".join(lines), encoding="utf-8")


def detect_orphans(previous: set[Path], current: set[Path]) -> set[Path]:
    """Files in previous manifest but not in current generation."""
    return previous - current


def is_owned(path: Path, manifest: set[Path]) -> bool:
    """Check if the generator owns this file."""
    return path in manifest


def merge_manifest(repo_root: Path, processed_domains: set[str], new_entries: set[Path]) -> set[Path]:
    """Merge new entries for processed domains without touching unprocessed ones.

    Used by --domain filter to avoid marking unprocessed domains as orphans.
    """
    existing = load_manifest(repo_root)

    preserved = {p for p in existing if not _path_matches_domains(p, processed_domains)}
    merged = preserved | new_entries
    return merged


def _path_matches_domains(path: Path, domains: set[str]) -> bool:
    """Check if a file path belongs to one of the given domains."""
    path_str = str(path)
    for domain in domains:
        if f"/{domain}." in path_str or f"/{domain}/" in path_str or path_str.endswith(f"/{domain}.py"):
            return True
    return False
