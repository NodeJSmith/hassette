def _find_class(module: ast.Module, name: str, source_label: str) -> ast.ClassDef:
    """Find a class by name in a parsed module, raising SystemExit if not found."""
    for node in module.body:
        if isinstance(node, ast.ClassDef) and node.name == name:
            return node
    raise SystemExit(f"Could not find class `{name}` in {source_label}")


def _module_level_assigned_names(module: ast.Module) -> set[str]:
    """Collect names assigned at module level (constants, dispatch dicts) — not imports.

    These cannot be imported into the generated facade module, because ``recording_api.py``
    imports ``RecordingSyncFacade`` from the generated module at runtime — a reverse
    (non-``TYPE_CHECKING``) import back into ``recording_api.py`` would be circular. A
    body-copied method that references one of these names must be stubbed instead.
    """
    names: set[str] = set()
    for node in module.body:
        if isinstance(node, ast.Assign):
            for target in node.targets:
                if isinstance(target, ast.Name):
                    names.add(target.id)
        elif isinstance(node, ast.AnnAssign) and isinstance(node.target, ast.Name):
            names.add(node.target.id)
    return names


def _references_names(func: ast.AsyncFunctionDef | ast.FunctionDef, names: set[str]) -> bool:
    """Return True if any ``ast.Name`` in ``func`` (signature or body) is in ``names``."""
    return any(isinstance(node, ast.Name) and node.id in names for node in ast.walk(func))


def _calls_self_attr(func: ast.AsyncFunctionDef | ast.FunctionDef, names: "set[str] | frozenset[str]") -> bool:
    """Return True if ``func`` calls ``self._parent.<name>(...)`` for any ``name`` in ``names``.

    Used to detect calls to async peer methods that live one level above the recording
    class being processed (e.g. ``RecordingHelperClient.increment`` calling
    ``self._parent.call_service(...)``, where ``call_service`` is async on the *grandparent*
    ``RecordingApi`` — ``self._parent`` inside ``RecordingHelperClient`` already refers to its
    owning ``RecordingApi``). After body-copying into the nested
    ``RecordingHelperClientSyncFacade``, the rewriter turns this into
    ``self._parent._parent.X()`` (two levels deep), which survives
    ``gen_recording_method``'s single-level ``self._parent.X()`` peer-call check and would
    otherwise be silently body-copied as an un-awaited coroutine call. This function inspects
    the *original*, pre-rewrite body (matching the single-level ``self._parent.X()`` shape as
    written in ``recording_api.py``) so the un-copyable case is caught before rewriting.
    """
    for node in ast.walk(func):
        if not isinstance(node, ast.Call):
            continue
        callee = node.func
        if not isinstance(callee, ast.Attribute) or callee.attr not in names:
            continue
        callee_value = callee.value
        if (
            isinstance(callee_value, ast.Attribute)
            and callee_value.attr == "_parent"
            and isinstance(callee_value.value, ast.Name)
            and callee_value.value.id == "self"
        ):
            return True
    return False


def _generate_recording_facade_methods(
    source_path: Path,
    source_class_name: str,
    recording_module: ast.Module,
    recording_class_name: str,
    recording_source_label: str,
    extra_async_method_names: "set[str] | frozenset[str]" = _NO_EXTRA_ASYNC_METHOD_NAMES,
) -> tuple[str, set[str], set[str], set[str]]:
    """Generate the body-copied/stubbed methods for one (source, recording) class pair.

    Mirrors the per-pair logic shared by ``generate_sync_recording`` (Api/RecordingApi) and
    ``generate_sync_recording_helpers`` (HelperClient/RecordingHelperClient).

    ``extra_async_method_names`` covers async methods that live one level above the
    recording class (e.g. ``RecordingApi.call_service`` when generating the nested
    ``RecordingHelperClient`` facade) — a method that calls one of these via
    ``self.<name>(...)`` is forced to the stub tier rather than body-copied, since the
    single-level peer-call check in ``gen_recording_method`` cannot see two levels deep.

    Returns:
        Tuple of (methods_source, body_symbols, runtime_annotation_symbols, string_ref_symbols).

    """
    source = source_path.read_text(encoding="utf8")
    module = safe_parse(source, str(source_path))
    source_class = _find_class(module, source_class_name, str(source_path))

    # Collect all public async-or-Coroutine methods on the source class (ordered, no
    # overloads, no lifecycle). Includes plain ``def -> Coroutine[...]`` methods introduced
    # by design/071 de-asyncing.
    source_methods: list[ast.AsyncFunctionDef | ast.FunctionDef] = [
        node for node in source_class.body if is_wrappable(node)
    ]

    recording_class = _find_class(recording_module, recording_class_name, recording_source_label)

    # Build map of async method name -> AST node for the recording class.
    recording_async_map: dict[str, ast.AsyncFunctionDef | ast.FunctionDef] = {}
    for node in recording_class.body:
        if isinstance(node, ast.AsyncFunctionDef):
            recording_async_map[node.name] = node
        elif isinstance(node, ast.FunctionDef) and has_coroutine_return_annotation(node):
            recording_async_map[node.name] = node

    # Collect ALL async method names (for the peer-call static check)
    async_method_names: set[str] = set(recording_async_map.keys())

    # Module-level constants/dispatch dicts defined in recording_api.py itself (not
    # imported) — a body-copied method referencing one of these can't be safely emitted,
    # since recording_api.py imports the generated module at runtime (circular import).
    recording_local_names = _module_level_assigned_names(recording_module)

    generated_methods: list[str] = []
    all_body_nodes: list[ast.stmt] = []
    all_runtime_annotation_symbols: set[str] = set()
    all_string_ref_symbols: set[str] = set()

    for src_func in source_methods:
        method_name = src_func.name
        rec_func = recording_async_map.get(method_name)

        # Determine whether to body-copy or stub.
        use_body_copy = (
            rec_func is not None
            and not is_not_implemented_only(rec_func)
            and not _references_names(rec_func, recording_local_names)
            and not _calls_self_attr(rec_func, extra_async_method_names)
        )

        if use_body_copy:
            assert rec_func is not None
            runtime_syms, string_syms = collect_annotation_symbols(rec_func)
            all_runtime_annotation_symbols |= runtime_syms
            all_string_ref_symbols |= string_syms
            method_src, body_nodes = gen_recording_method(rec_func, async_method_names)
            generated_methods.append(method_src)
            all_body_nodes.extend(body_nodes)
        else:
            # Stub — use the source-class signature for the stub type annotations
            runtime_syms, string_syms = collect_annotation_symbols(src_func)
            all_runtime_annotation_symbols |= runtime_syms
            all_string_ref_symbols |= string_syms
            method_src = gen_recording_stub(src_func)
            generated_methods.append(method_src)

    body_symbols = _collect_referenced_symbols(all_body_nodes)

    # Optional: warn about signature default mismatches. Only applies to body-copied
    # methods — stub-tier methods inherit their signature from the source class and never
    # carry recording-class defaults, so a "mismatch" there is a false positive.
    for src_func in source_methods:
        if src_func.name in recording_async_map:
            rec_func_check = recording_async_map[src_func.name]
            if (
                is_not_implemented_only(rec_func_check)
                or _references_names(rec_func_check, recording_local_names)
                or _calls_self_attr(rec_func_check, extra_async_method_names)
            ):
                continue
            src_defaults = [ast.unparse(d) for d in src_func.args.defaults]
            rec_defaults = [ast.unparse(d) for d in rec_func_check.args.defaults]
            if src_defaults != rec_defaults:
                print(
                    f"Warning: default mismatch for `{src_func.name}`: "
                    f"{source_class_name} defaults={src_defaults!r}, {recording_class_name} defaults={rec_defaults!r}",
                    file=sys.stderr,
                )

    return "\n".join(generated_methods), body_symbols, all_runtime_annotation_symbols, all_string_ref_symbols


def _build_import_blocks(
    needed_symbols: set[str],
    string_ref_symbols: set[str],
    symbol_maps: list[dict[str, str]],
    tc_maps: list[dict[str, str]],
) -> tuple[str, str]:
    """Build (import_block, type_checking_imports) from merged symbol maps.

    ``symbol_maps``/``tc_maps`` are merged left-to-right — later maps take precedence,
    matching the "recording_api takes precedence" convention used throughout this module.
    """
    combined_symbol_map: dict[str, str] = {}
    for m in symbol_maps:
        combined_symbol_map.update(m)

    combined_tc_map: dict[str, str] = {}
    for m in tc_maps:
        combined_tc_map.update(m)

    import_block = build_precise_import_block(needed_symbols, combined_symbol_map)

    type_checking_symbols = string_ref_symbols - needed_symbols
    type_checking_extra_lines: list[str] = []
    for sym in sorted(type_checking_symbols):
        source_map = combined_tc_map if sym in combined_tc_map else combined_symbol_map
        precise = build_precise_import_block({sym}, source_map)
        if precise:
            type_checking_extra_lines.append(f"    {precise}")
    type_checking_imports = ("\n" + "\n".join(type_checking_extra_lines) + "\n") if type_checking_extra_lines else "\n"

    return import_block, type_checking_imports


def _async_method_names_of(module: ast.Module, class_name: str, source_label: str) -> frozenset[str]:
    """Return the async (or Coroutine-returning) method names of a class in a parsed module."""
    class_def = _find_class(module, class_name, source_label)
    names: set[str] = set()
    for node in class_def.body:
        if isinstance(node, ast.AsyncFunctionDef):
            names.add(node.name)
        elif isinstance(node, ast.FunctionDef) and has_coroutine_return_annotation(node):
            names.add(node.name)
    return frozenset(names)


def generate_sync_recording(api_path: Path, recording_api_path: Path) -> str:
    """Generate the full RecordingSyncFacade source (including nested helper facade).

    Imports are derived via ``_collect_referenced_symbols`` + ``build_precise_import_block``
    directly. This pair is **lenient** about unknown symbols (it skips any name that doesn't
    appear in the symbol map) because body-copied method bodies reference many lowercase
    symbols that are local variables, parameters, or comprehension targets — not imports. The
    strict variant ``_derive_recording_imports_strict`` is reserved for unit tests that need
    to assert the error path; the post-generation ``ruff check`` + ``py_compile`` steps are
    the safety net for genuinely missing imports in production.

    The output combines two classes in one file: ``RecordingSyncFacade`` (wrapping
    ``RecordingApi``) and ``RecordingHelperClientSyncFacade`` (wrapping
    ``RecordingHelperClient``, exposed as ``RecordingSyncFacade.helpers``) — mirroring how
    ``ApiSyncFacade.helpers`` wraps ``HelperClientSyncFacade`` in the async-side facade.

    Args:
        api_path: Path to src/hassette/api/api.py (source of truth for the Api method list).
        recording_api_path: Path to src/hassette/test_utils/recording_api.py.

    Returns:
        Complete Python source string for the generated sync_facade.py.

    """
    api_source = api_path.read_text(encoding="utf8")
    recording_source = recording_api_path.read_text(encoding="utf8")
    recording_module = safe_parse(recording_source, str(recording_api_path))

    helpers_path = api_path.with_name("helpers.py")
    helpers_source = helpers_path.read_text(encoding="utf8")

    # RecordingHelperClient methods (increment/decrement/reset) delegate to the grandparent
    # RecordingApi's async call_service — see _calls_self_attr's docstring for why this must
    # be passed explicitly rather than discovered from RecordingHelperClient's own body.
    recording_api_async_names = _async_method_names_of(recording_module, "RecordingApi", str(recording_api_path))

    api_methods_str, api_body_syms, api_runtime_syms, api_string_syms = _generate_recording_facade_methods(
        api_path, "Api", recording_module, "RecordingApi", str(recording_api_path)
    )
    helpers_methods_str, helpers_body_syms, helpers_runtime_syms, helpers_string_syms = (
        _generate_recording_facade_methods(
            helpers_path,
            "HelperClient",
            recording_module,
            "RecordingHelperClient",
            str(recording_api_path),
            extra_async_method_names=recording_api_async_names,
        )
    )

    needed_symbols = (
        api_body_syms | api_runtime_syms | helpers_body_syms | helpers_runtime_syms
    ) - FIXED_HEADER_SYMBOLS
    string_ref_symbols = (api_string_syms | helpers_string_syms) - FIXED_HEADER_SYMBOLS - needed_symbols

    import_block, type_checking_imports = _build_import_blocks(
        needed_symbols,
        string_ref_symbols,
        symbol_maps=[
            _HELPERS_EXTRA_SYMBOL_MAP,
            collect_module_level_import_map(api_source),
            collect_module_level_import_map(helpers_source),
            collect_module_level_import_map(recording_source),
        ],
        tc_maps=[
            collect_type_checking_import_map(api_source),
            collect_type_checking_import_map(helpers_source),
            collect_type_checking_import_map(recording_source),
        ],
    )

    return (
        _RECORDING_MODULE_HEADER_TEMPLATE.format(
            imports=import_block,
            type_checking_imports=type_checking_imports,
        )
        + _RECORDING_CLASS_HEADER
        + "\n"
        + api_methods_str
        + "\n\n"
        + _RECORDING_HELPERS_CLASS_HEADER
        + "\n"
        + helpers_methods_str
    )


def generate_sync_recording_helpers(helpers_path: Path, recording_api_path: Path) -> str:
    """Generate a standalone RecordingHelperClientSyncFacade module source.

    Follows the same generation pattern as ``generate_sync_recording`` (parse the source
    class' wrappable methods, correlate by name against the recording class, body-copy or
    stub, derive imports), but scoped to the ``HelperClient``/``RecordingHelperClient`` pair.

    Not used directly as a CLI output target — ``generate_sync_recording`` embeds the same
    generated class into the combined ``test_utils/sync_facade.py`` file so that
    ``RecordingSyncFacade.helpers`` and ``ApiSyncFacade.helpers`` mirror each other in one
    file per the existing one-facade-per-file convention. This function is exposed standalone
    for unit testing and to keep call-shape parity with ``generate_sync_recording``.

    Args:
        helpers_path: Path to src/hassette/api/helpers.py (source of truth for the
            HelperClient method list).
        recording_api_path: Path to src/hassette/test_utils/recording_api.py.

    Returns:
        Complete Python source string for a standalone RecordingHelperClientSyncFacade module.

    """
    helpers_source = helpers_path.read_text(encoding="utf8")
    recording_source = recording_api_path.read_text(encoding="utf8")
    recording_module = safe_parse(recording_source, str(recording_api_path))

    recording_api_async_names = _async_method_names_of(recording_module, "RecordingApi", str(recording_api_path))

    methods_str, body_symbols, runtime_syms, string_syms = _generate_recording_facade_methods(
        helpers_path,
        "HelperClient",
        recording_module,
        "RecordingHelperClient",
        str(recording_api_path),
        extra_async_method_names=recording_api_async_names,
    )

    needed_symbols = (body_symbols | runtime_syms) - _HELPERS_FIXED_HEADER_SYMBOLS
    string_ref_symbols = string_syms - _HELPERS_FIXED_HEADER_SYMBOLS - needed_symbols

    import_block, type_checking_imports = _build_import_blocks(
        needed_symbols,
        string_ref_symbols,
        symbol_maps=[
            _HELPERS_EXTRA_SYMBOL_MAP,
            collect_module_level_import_map(helpers_source),
            collect_module_level_import_map(recording_source),
        ],
        tc_maps=[
            collect_type_checking_import_map(helpers_source),
            collect_type_checking_import_map(recording_source),
        ],
    )

    return (
        _RECORDING_HELPERS_MODULE_HEADER_TEMPLATE.format(
            imports=import_block,
            type_checking_imports=type_checking_imports,
        )
        + _RECORDING_HELPERS_CLASS_HEADER
        + "\n"
        + methods_str
    )
